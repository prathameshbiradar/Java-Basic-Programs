package task1;

public class AssignmentOperator {

	public static void main(String[] args) {
		//Assignment operators : = , +=, -=, *=, /=, %= 
		
		int num1 = 13; // = operator
		System.out.println(num1);
		num1 += 25;
		System.out.println(num1);
		num1 -= 25;
		System.out.println(num1);
		num1 *= 2;
		System.out.println(num1);
		num1 /= 2;
		System.out.println(num1);
		num1 %= 5;
		System.out.println(num1);

	}

}
